void display_update(void) {
	int i, j, k;
	int c;
	for(i = 0; i < 4; i++) {
		COMMAND_MODE;
		SPI2send(0x22);
		SPI2send(i);
		
		SPI2send(0x0);
		SPI2send(0x10);
		
		DATA_MODE;
		
		for(j = 0; j < 16; j++) {
			c = textbuffer[i][j];
			if(c & 0x80)
				continue;
			
			for(k = 0; k < 8; k++)
				SPI2send(font[c*8 + k]);
		}
	}
}
